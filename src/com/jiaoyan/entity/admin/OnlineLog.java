package com.jiaoyan.entity.admin;

public class OnlineLog {

	private int ONLINE_ID;
	private String ONLINE_USERID;
	private String ONLINE_USERNAME;
	private String BEGIN_DATE;
	private String END_DATE;
	private String ONLINE_COUNTE;
	private String USER_IP;
	
	public int getONLINE_ID() {
		return ONLINE_ID;
	}
	public void setONLINE_ID(int oNLINE_ID) {
		ONLINE_ID = oNLINE_ID;
	}
	public String getONLINE_USERID() {
		return ONLINE_USERID;
	}
	public void setONLINE_USERID(String oNLINE_USERID) {
		ONLINE_USERID = oNLINE_USERID;
	}
	public String getONLINE_USERNAME() {
		return ONLINE_USERNAME;
	}
	public void setONLINE_USERNAME(String oNLINE_USERNAME) {
		ONLINE_USERNAME = oNLINE_USERNAME;
	}
	public String getBEGIN_DATE() {
		return BEGIN_DATE;
	}
	public void setBEGIN_DATE(String bEGIN_DATE) {
		BEGIN_DATE = bEGIN_DATE;
	}
	public String getEND_DATE() {
		return END_DATE;
	}
	public void setEND_DATE(String eND_DATE) {
		END_DATE = eND_DATE;
	}
	public String getONLINE_COUNTE() {
		return ONLINE_COUNTE;
	}
	public void setONLINE_COUNTE(String oNLINE_COUNTE) {
		ONLINE_COUNTE = oNLINE_COUNTE;
	}
	public String getUSER_IP() {
		return USER_IP;
	}
	public void setUSER_IP(String uSER_IP) {
		USER_IP = uSER_IP;
	}
	
	
}
